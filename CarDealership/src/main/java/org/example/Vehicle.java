package org.example;

public class Vehicle {
    public int vin;
    public int year;
    public String make;
    public String model;
    public String vehicleType;
    public String color;
    public int mileage;
    public double price;
    public String type;

    public Vehicle(int vin, int year, String make, String model, String vehicleType, String color, int mileage, double price) {
        this.vin = vin;
        this.year = year;
        this.make = make;
        this.model = model;
        this.vehicleType = vehicleType;
        this.color = color;
        this.mileage = mileage;
        this.price = price;
    }
    public int getVin() {
        return vin;
    }

    public void setVin(int vin) {
        this.vin = vin;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getVehicleType() {
        return vehicleType;
    }

    public void setVehicleType(String vehicleType) {
        this.vehicleType = vehicleType;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getMileage() {
        return mileage;
    }

    public void setMileage(int mileage) {
        this.mileage = mileage;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getType() {
        return type;
    }

    @Override
    public String toString(){
        return String.format("%d | %d | %s | %s | %s | %s | %d | %f ", this.getVin(), this.getYear(), this.getMake(), this.getModel(), this.getColor(), this.getVehicleType(), this.getMileage(), this.getPrice() );

    }

    }



